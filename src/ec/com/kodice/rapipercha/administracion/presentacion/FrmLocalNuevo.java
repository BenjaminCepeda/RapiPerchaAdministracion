    private void cargarDatos() {
        LocalVO localVO = null;
        LocalBO localBO = new LocalBO();
        CentroExpendioBO centroExpendioBO = new CentroExpendioBO();
        try {
            cmbCenExpendio.setModel(centroExpendioBO.generaModeloDatosCombo());
            if (codigoActual != 0) {
                localVO = localBO.buscar(codigoActual);
            }
        } catch (Exception e) {
            UtilPresentacion.mostrarMensajeError(this, e.getMessage());
        } finally {
            if (localVO != null) {
                cmbCenExpendio.getModel().setSelectedItem(localVO.getCentroExpendioVO().getCodigo());
                txtCodigo.setText(String.valueOf(localVO.getCodigo()));
                txtNombre.setText(localVO.getNombre());
                txtDireccion.setText(localVO.getDireccion());
            }
            localVO = null;
            localBO = null;
        }
    }

