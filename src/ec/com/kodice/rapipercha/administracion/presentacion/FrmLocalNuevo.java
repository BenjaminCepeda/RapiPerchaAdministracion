    private void btnGrabarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGrabarActionPerformed
        LocalVO localVO = null;
        LocalBO localBO = null;
        CentroExpendioVO centroExpendioVO = null;
        CentroExpendioBO centroExpendioBO = null;
        int respuestaOperacion = 0;
        boolean camposValidos;
        camposValidos
                = (!txtNombre.getText().isEmpty()
                && !txtNombre.getText().trim().equals("")
                && !txtDireccion.getText().isEmpty()
                && !txtDireccion.getText().trim().equals(""));
        if (camposValidos) {
            localBO = new LocalBO();
            localVO = new LocalVO(codigoActual, txtNombre.getText(),
                    txtDireccion.getText());
            try {
                centroExpendioVO = (CentroExpendioVO) cmbCenExpendio.getSelectedItem();
                localVO.setCentroExpendioVO(centroExpendioVO);
                if (codigoActual == 0) {
                    respuestaOperacion = localBO.crear(localVO);
                    txtCodigo.setText(String.valueOf(respuestaOperacion));
                } else {
                    respuestaOperacion = localBO.actualizar(localVO);
                }
                if (respuestaOperacion > 0) {
                    JOptionPane.showMessageDialog(null, "Registro guardado con éxito");
                }
            } catch (Exception e) {
                UtilPresentacion.mostrarMensajeError(this, e.getMessage());
            } finally {
                localVO = null;
                localBO = null;
                centroExpendioVO = null;
                centroExpendioBO = null;
                this.setVisible(false);
                this.dispose();
            }
        } else {
            UtilPresentacion.mostrarMensajeValidacionIncorrecta(this,
                    "Ingrese los datos requeridos en el formulario");
        }
    }//GEN-LAST:event_btnGrabarActionPerformed

