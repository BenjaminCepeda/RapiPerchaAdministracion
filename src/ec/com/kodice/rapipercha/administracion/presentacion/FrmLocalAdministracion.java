    private void tblLocalMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblLocalMousePressed
        // TODO add your handling code here:
        JTable source = (JTable) evt.getSource();
        String codigoElegido = "";
        int fila = source.rowAtPoint(evt.getPoint());
        if (fila >= 0) {
            codigoElegido = tblLocal.getModel().getValueAt(
                    fila, 0).toString();
            btnConsultar.setEnabled(true);
            btnEditar.setEnabled(true);
            btnBorrar.setEnabled(true);
            if (evt.getClickCount() == 2 && !codigoElegido.isEmpty() && !codigoElegido.isBlank()) {
                FrmLocalNuevo frmLocalNuevo = new FrmLocalNuevo(
                        Integer.valueOf(codigoElegido),true);
                frmLocalNuevo.setVisible(true);
                cargarModelo();
            }
        }
    }//GEN-LAST:event_tblLocalMousePressed

