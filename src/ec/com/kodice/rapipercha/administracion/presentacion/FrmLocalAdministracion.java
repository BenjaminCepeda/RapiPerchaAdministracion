    private void btnConsultarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConsultarActionPerformed
        // TODO add your handling code here:
        String codigoElegido = "";
        int fila = tblLocal.getSelectedRow();
        if (fila >= 0) {
            codigoElegido = tblLocal.getModel().getValueAt(
                fila, 0).toString();
        }
        if (!(codigoElegido.isEmpty() | codigoElegido.isBlank())) {
            FrmLocalNuevo frmLocalNuevo = new FrmLocalNuevo(
                Integer.valueOf(codigoElegido), true);
            frmLocalNuevo.setVisible(true);
            cargarModelo();
        }
    }//GEN-LAST:event_btnConsultarActionPerformed

